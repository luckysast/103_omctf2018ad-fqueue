def build_sr():
    sr1 = np.zeros(shape=(32, 32), dtype=int)
    for a, b in zip(range(0, 17, 8), range(8, 25, 8)):
        sr1[a:b, a + 8:b + 8] = np.identity(8, dtype=int)
    sr1[24:32, 0:8] = np.identity(8, dtype=int)

    # SR2 = SR1 * SR1, SR2 - 32x32 binary matrix
    sr2 = sr1.dot(sr1) % 2

    # SR3 = SR2 * SR1, SR3 - 32x32 binary matrix
    sr3 = sr2.dot(sr1) % 2

    sr = np.zeros(shape=(128, 128), dtype=int)
    sr[0:32, 0:32] = np.identity(32, dtype=int)
    sr[32:64, 32:64] = sr1
    sr[64:96, 64:96] = sr2
    sr[96:128, 96:128] = sr3

    return sr


